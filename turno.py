print('Vamos para o turno que você estuda \n')

turno = input('Qual  turno que você estuda: \n')


if turno == 'm' or turno == 'M':
  print('Bom dia!\n')
elif turno == 'v' or turno == 'V':
  print('Boa tarde!\n')
elif turno == 'n' or turno == 'N':
  print('Boa noite!\n')
else:
  print('Valor invalido!')